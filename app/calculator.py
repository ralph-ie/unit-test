class Calculator:

    @staticmethod
    def addition(val1, val2):
        return val1 + val2

    @staticmethod
    def subtraction(val1, val2):
        return val1 - val2

    @staticmethod
    def multiplication(val1, val2):
        return val1 * val2

    @staticmethod
    def division(val1, val2):
        return val1 / val2
